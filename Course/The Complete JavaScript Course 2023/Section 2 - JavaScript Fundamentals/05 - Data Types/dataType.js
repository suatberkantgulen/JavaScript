// JavaScript has dynamic typing
// This means that when you create a new variable, you do not have to manually define the data type of the value that it contains.
// In many other programming languages, you actually have to do that but not in JavaScript. Instead, Java script automatically determines the data type of a value when it's stored into a variable.
// !!! And this distinction between value and variable is pretty important because in JavaScript, it's the value that has a type, not the variable. So variables simply store values that have a type.
// Now another important application of dynamic typing is that later in our code, we can assign a new value with a different data type to the same variable without a problem.