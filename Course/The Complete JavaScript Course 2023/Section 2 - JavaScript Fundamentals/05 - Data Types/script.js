
// type o  f
// So type of it's an operator just like the plus or the minus operator that we can use to show the type of a value.

let javaScriptIsFun = true;

console.log(typeof true);
console.log(typeof 23);
console.log(typeof "Berkant");
console.log(typeof 'Gulen');
console.log(typeof javaScriptIsFun);

// Dynamic typing
javaScriptIsFun = 'YES!';

// So the first time that we declare a new variable we need to do it using let but then when we want to change the value of the variable, we simply write it again like this but without the let.

console.log(typeof javaScriptIsFun);

// Undefined
let year;
console.log(year);
console.log(typeof year);

// So simply put undefined is both the value and the type of the value and undefined is different than other types in this way.

year = 2023;
console.log(year);
console.log(typeof year);

// Null
console.log(typeof null); // Object